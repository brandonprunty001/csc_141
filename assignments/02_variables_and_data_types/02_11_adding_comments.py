# my name is brandon and today's date is 9/11/2025
# this program shows how to remove whitespace from a name using lstrip

name = "\t\n    brandon    \n\t" # name with tabs and newlines

print("original with whitespace")
print(name)

print("\nusing lstrtip():")
print(name.lstrip())

print("\nusing rstrip():")
print(name.rstrip())

print("\nusing strip():")
print(name. strip())